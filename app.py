@app.route("/")
def index():
    return render_template("index.html", minerals=MINERALS)

@app.route("/api/mineral/<mineral_id>")
def get_mineral(mineral_id):
    for m in MINERALS:
        if m["id"] == mineral_id:
            return jsonify(m)
    return jsonify({"error": "not found"}), 404

@app.route("/static/minerals/<filename>")
def mineral_image(filename):
    return send_from_directory("static/minerals", filename)

